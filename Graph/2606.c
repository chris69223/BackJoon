#include <stdio.h>

int n; // 정점의 총 갯수
int rear = 0, front = 0;
int map[1001][1001], queue[1001], BFSvisit[1001]; // 인접 행렬과 방문 여부를 나타내는 배열
int cnt = 0;

void BFS(int v)
{
	int i, s;
	queue[0] = v;
	rear++;
	BFSvisit[v] = 1;
	//queue[rear++] = v;
	while (front < rear) // 후단이 전단과 같거나 작으면 루프 탈출
	{
		// 큐의 첫번째에 있는 데이터를 제외하고 제외된 값을 가져오며, 전단 1 증가
		s = queue[front];
		front++;
		for (i = 1; i <= n; i++)
		{

			// 정점 v와 정점 i가 만나고, 정점 i를 방문하지 않은 상태일 경우
			if (map[s][i] == 1 && BFSvisit[i] == 0)
			{
				BFSvisit[i] = 1;
				cnt++;
				// 정점 i를 방문했다고 표시
				queue[rear] = i;//후단을 증가시킴
				rear++;

			}
		}
	}
	printf("%d", cnt);
}

int main()
{
	
	int v1, v2; //인접행렬에 넣을 행,열의 입력값
	int g;//간선의 개수 

	scanf("%d", &n);//정점의 갯수
	scanf("%d", &g);//간선의 갯수 
	for (int i = 0; i < g; i++) //인접행렬 만들기!
	{
		scanf("%d %d", &v1, &v2);
		map[v1][v2] = map[v2][v1] = 1; // 정점 v1과 정점 v2가 연결되었다고 표시

	} //행렬 다 만들고,

	BFS(1);
	return 0;
}


